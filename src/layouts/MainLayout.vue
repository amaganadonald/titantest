<script lang="ts">
import { defineComponent, onBeforeMount, onMounted, ref, computed } from 'vue';
import { useI18n } from 'vue-i18n';
import { useQuasar } from 'quasar';
import appjs from '../assets/js/app.js';
import { useRouter } from 'vue-router/dist/vue-router';
import { useMaintenanceStore } from 'src/stores/maintenance-store';

export default defineComponent({
  name: 'MainLayout',
  components: {},
  setup() {
    const $q = useQuasar();
    const router = useRouter();
    const store = useMaintenanceStore();
    let data = computed(() => store.dataEntretienStatus);
    let docExpire = computed(() => store.docExpire);
    const menu = ref([]);
    let usr = ref('');
    let profile = ref('');
    let ig = ref('');
    let host = ref('');
    let hts = ref('');
    const getImageUrl = (img: any) => {
      return new URL(`../assets/images/${img}`, import.meta.url);
    };
    /*const getImageUrl2 = (img: any) => {
      if (img != null) {
        //return new URL(`http://localhost:4000${img}`, import.meta.url);
        return new URL(`${img}`, import.meta.url);
      }
    };*/
    let icon = ref('');
    const { locale, t } = useI18n({ useScope: 'global' });
    const leaveApps = () => {
      $q.cookies.set('lang', '');
      $q.cookies.set('tk', '');
      $q.cookies.set('user', '');
      $q.cookies.set('id', '');
      $q.cookies.set('mail', '');
      $q.cookies.set('photo', '');
      $q.cookies.set('mem', '');
      $q.cookies.set('auth', '');
      $q.cookies.set('profs', '');
      $q.cookies.set('uidt988', '');
      $q.cookies.set('log', '');
      $q.cookies.set('ht', '');
      router.push('/').catch(() => {
        console.log('erreur navigation');
      });
    };

    const goPlanning = () => {
      router.push('/entretien').catch(() => {
        console.log('erreur navigation');
      });
    };

    const chargeFr = () => {
      icon.value = getImageUrl('france.svg');
      $q.cookies.set('lang', 'fr');
      locale.value = 'fr';
      return icon;
    };

    const chargeEn = () => {
      icon.value = getImageUrl('en.svg');
      locale.value = 'en-US';
      $q.cookies.set('lang', 'en-US');
      return icon;
    };
    onBeforeMount(() => {
      usr.value = $q.cookies.get('log');
      profile.value = $q.cookies.get('profs');
      ig.value = $q.cookies.get('phot');
      locale.value = $q.cookies.get('lang');
      menu.value = $q.cookies.get('mem');
      host.value = $q.cookies.get('ht') + $q.cookies.get('phot');
      hts.value = $q.cookies.get('ht');
      if (locale.value === 'fr') {
        chargeFr();
      } else {
        chargeEn();
      }
    });
    onMounted(async () => {
      await appjs();
      await store.allEntretienStatus();
      await store.allDocumentsExpire();
    });
    return {
      chargeEn,
      chargeFr,
      leaveApps,
      icon,
      menu,
      locale,
      usr,
      profile,
      ig,
      host,
      data,
      goPlanning,
      hts,
      docExpire,
    };
  },
});
